def parse_vocab(text: str) -> dict[int, list[list[str]]]:
    by_story: dict[int, list[list[str]]] = {}
    for line in text.strip().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        story_id, no, word, reading, part, meaning = line.split("|")
        by_story.setdefault(int(story_id), []).append([no, word, reading, part, meaning])
    return by_story


