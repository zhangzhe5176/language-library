def main():
    text = DATA.read_text(encoding="utf-8")
    prefix, raw = text.split("=", 1)
    data = json.loads(raw.strip().rstrip(";"))
    by_id = {story["id"]: story for story in data["stories"]}
    missing = sorted(set(PATCHES) - set(by_id))
    if missing:
        raise SystemExit(f"Missing story IDs: {missing}")
    for story_id, patch in PATCHES.items():
        story = by_id[story_id]
        story.update(patch)
        story["reviewStatus"] = "已校对"
    DATA.write_text(
        prefix + "= " + json.dumps(data, ensure_ascii=False, separators=(",", ":")) + ";\n",
        encoding="utf-8",
    )


if __name__ == "__main__":
    main()
