def main():
    text = DATA.read_text(encoding="utf-8")
    prefix, raw = text.split("=", 1)
    data = json.loads(raw.strip().rstrip(";"))

    seen = set()
    for story in data["stories"]:
        if story.get("topicId") not in {11, 12, 13}:
            continue
        new_vocab = []
        for row in story.get("vocab", []):
            no = str(row[0])
            if no in VOCAB_BY_NO:
                new_vocab.append(VOCAB_BY_NO[no])
                seen.add(no)
            else:
                new_vocab.append(row)
        story["vocab"] = new_vocab

    missing = sorted(set(VOCAB_BY_NO) - seen, key=int)
    if missing:
        raise SystemExit(f"Missing vocab numbers: {missing}")

    DATA.write_text(
        prefix + "= " + json.dumps(data, ensure_ascii=False, separators=(",", ":")) + ";\n",
        encoding="utf-8",
    )


if __name__ == "__main__":
    main()
